/**
 * @param {number[][]} grid
 * @return {number}
 */
var islandPerimeter = function (grid) {
    let hash = {};
    for (let i = 0; i < grid.length; i++) {
        for (let j = 0; j < grid[i].length; j++) {
            let isLand = grid[i][j];
            hash[`${i},${j}`] = grid[i][j] ? 4 : 0;
            if (isLand && hash[`${i},${j - 1}`]) {
                hash[`${i},${j - 1}`] -= 1;
                hash[`${i},${j}`] -= 1;
            }
            if (isLand && hash[`${i - 1},${j}`]) {
                hash[`${i - 1},${j}`] -= 1;
                hash[`${i},${j}`] -= 1;
            }
        }
    }
    return Object.values(hash).reduce((a, b) => a + b, 0);
};
